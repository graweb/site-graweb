  data() {
    return {
      formMessage: false,
      form: {
        name: "",
        email: "",
        message: "",
      },
      validation: {
        name: false,
        email: false,
        message: false,
      },
    };
  },
  methods: {
    formSubmit(e) {
      if (this.form.name == "") {
        this.validation.name = true;
        setTimeout(() => {
          this.validation.name = false;
        }, 3000);
      } else if (this.form.email == "") {
        this.validation.email = true;
        setTimeout(() => {
          this.validation.email = false;
        }, 3000);
      } else if (this.form.message == "") {
        this.validation.message = true;
        setTimeout(() => {
          this.validation.message = false;
        }, 3000);
      } else {
        e.preventDefault();
        fetch("http://www.graweb.com.br/send.php", {
          method: "post",
          body: new URLSearchParams(this.form),
        });

        this.formMessage = true;

        setTimeout(() => {
          this.formMessage = false;
          this.form = [];
        }, 3000);
      }
    },
  },
};
